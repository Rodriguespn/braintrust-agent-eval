/**
 * Agent interface and common types for all agents.
 */

import type { AgentType, ModelTier, SetupFunction, TeardownFunction, SandboxBackend } from '../types.js';

/**
 * Common options for all agents.
 */
export interface AgentRunOptions {
  /** The prompt/task for the agent */
  prompt: string;
  /** Model to use (agent-specific) */
  model: ModelTier;
  /** Timeout in milliseconds */
  timeout: number;
  /** API key for the agent */
  apiKey: string;
  /** Optional setup function to run before agent */
  setup?: SetupFunction;
  /** Optional teardown function to run after agent finishes, before sandbox destruction */
  teardown?: TeardownFunction;
  /** npm scripts to run after agent completes */
  scripts?: string[];
  /** Abort signal to cancel the run */
  signal?: AbortSignal;
  /** Sandbox backend to use */
  sandbox?: SandboxBackend;
  /** Linux capabilities to add to the sandbox container (e.g. ['NET_ADMIN']) */
  capAdd?: string[];
  /** Kernel sysctls to set on the sandbox container */
  sysctls?: Record<string, string>;
  /** Docker network mode (e.g. 'host' to share host network namespace) */
  networkMode?: string;
  /** Callback for phase lifecycle events, used for tracing/observability */
  onPhase?: (name: string, status: 'start' | 'end', durationMs?: number) => void;
}

/**
 * Result from running a script.
 */
export interface ScriptResult {
  success: boolean;
  output: string;
}

/**
 * Result of an agent run.
 */
export interface AgentRunResult {
  /** Whether the run was successful */
  success: boolean;
  /** Raw output from the agent (stdout/stderr) */
  output: string;
  /** Structured transcript (JSONL format for Claude, JSON for Codex) */
  transcript?: string;
  /** Error message if failed */
  error?: string;
  /** Duration in milliseconds */
  duration: number;
  /** Results from configured scripts */
  scriptsResults?: Record<string, ScriptResult>;
  /** Sandbox ID for debugging */
  sandboxId?: string;
  /** Files generated by the agent */
  generatedFiles?: Record<string, string>;
  /** Files deleted by the agent */
  deletedFiles?: string[];
}

/**
 * Agent interface that all agents must implement.
 */
export interface Agent {
  /** Unique identifier for the agent */
  name: string;

  /** Human-readable display name */
  displayName: string;

  /** Run the agent on a fixture */
  run(fixturePath: string, options: AgentRunOptions): Promise<AgentRunResult>;

  /** Get agent-specific environment variable name for API key */
  getApiKeyEnvVar(): string;

  /** Get the default model for this agent */
  getDefaultModel(): ModelTier;
}

/**
 * Adapter metadata used by eval harnesses for agent-specific filesystem conventions.
 */
export interface AgentAdapter {
  /** Unique identifier for the agent */
  agentName: AgentType;
  /** Agent-specific skills directory in the sandbox, e.g. '.claude/skills' or '.codex/skills' */
  agentSkillsDir: string;
}
